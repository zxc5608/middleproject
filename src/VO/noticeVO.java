package VO;

public class noticeVO {
	private int ad_no        ;
	private String title        ;
	private String cont         ;
	private int notice_no    ;
	private String no_name      ;
	private String no_date      ;
	public int getAd_no() {
		return ad_no;
	}
	public void setAd_no(int ad_no) {
		this.ad_no = ad_no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCont() {
		return cont;
	}
	public void setCont(String cont) {
		this.cont = cont;
	}
	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public String getNo_name() {
		return no_name;
	}
	public void setNo_name(String no_name) {
		this.no_name = no_name;
	}
	public String getNo_date() {
		return no_date;
	}
	public void setNo_date(String no_date) {
		this.no_date = no_date;
	}
	
	
}
